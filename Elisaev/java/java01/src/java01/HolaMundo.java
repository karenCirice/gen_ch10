package java01;

public class HolaMundo {

	public static void main(String[] args) {
		System.out.println("Hola Mundo desde el IDE");
	//Definimos un variable booleana
		boolean result = true;
		//Variable de tipo caracter, se ebe escribir con comillas simples ''
		char capitalC = 'C';
		//Definimos una variable de tipo byte
		byte b = 100;
		//tipo short
		short s = 10000;
		//tipo int
		int i = 26;
		//variable tipo int en hexadecimal
		int hexVal = 0x1a;
		//variable tipo int en binario
		int binVal = 0b11010;
		
		//syso + ctrl + barra
		//ctrl + tab - encontrar variables
		System.out.println(result);
		System.out.println(capitalC);
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(hexVal);
		System.out.println(binVal);
	
	}

}
