package java01;

public class HolaMundo {

	public static void main(String[] args) {
		System.out.println("Hola Mundo de nuevo");
		// Definimos una variable booleana
		boolean result = true;
		//Definimos una variable de tipo caracter con el apostrofe
		char capitalC = 'C';
		//Definimos una variable tipo byte
		byte b = 100;
		//Definimos una variable de tipo short
		short s = 10000;
		//Definimos una variable de tipo int
		int i = 1000000;
		//Definimos una variable de tipo int, exadecimal (0x)
		int hexaVal = 0x1a;
		//Definimos una variable de tipo int, representacion binaria (0b)
		int binVal = 0b110110;
		
		
		
		//para llamarlo rapido se usa syso ctrl + espacio
		System.out.println(result);
		System.out.println(capitalC);
		System.out.println(b);
	}

}
